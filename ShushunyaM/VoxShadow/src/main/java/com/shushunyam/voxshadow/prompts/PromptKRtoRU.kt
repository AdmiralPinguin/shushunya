package com.shushunyam.voxshadow.prompts

/**
 * Шаблон промпта для перевода с корейского на русский.
 *
 * Здесь мы фиксируем стиль и формат, чтобы LLM отвечала строго.
 */
object PromptKRtoRU {

    fun build(text: String): String {
        return """
            Ты переводчик с корейского на русский.
            Переведи следующий текст максимально точно и нейтрально.
            Не добавляй комментарии, не меняй смысл.

            ТЕКСТ:
            $text
        """.trimIndent()
    }
}
